import Foundation

final class Calculator {

    func sum(_ a: Int, _ b: Int) -> Int {
        return a + b
    }

    func division(_ a: Int, _ b: Int) -> Int{
        return a / b
        
    }
    
    func substraction(_ a: Int, _ b: Int) -> Int{
        return a - b
    }
    
    func multiplication(_ a: Int, _ b: Int) -> Int {
        return a * b
    }
}
